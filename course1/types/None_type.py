# None is a special constant

print(None)

# It is impossible to do any math operations with None:
# print(None + 2)
# print(None * 3)

# None will be printed:
print('My name is {}'.format(None))

# Casting to other types:
print(str(None))
print(bool(None))

# But casting to int will fail:
# print(int(None))
